    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double horas_trabajadas = Double.parseDouble(txtHoras.getText());
        double valor_hora = Double.parseDouble(txtValor.getText());
        double retencion = Double.parseDouble(txtRetencion.getText());
        
        double salario_bruto = Nomina.calcular_bruto(horas_trabajadas, valor_hora);
        double salario_neto = Nomina.calcular_neto(horas_trabajadas, valor_hora, retencion);
        
        txtBruto.setText(String.valueOf(salario_bruto));
        txtNeto.setText(String.valueOf(salario_neto));
    }//GEN-LAST:event_btnCalcularActionPerformed

