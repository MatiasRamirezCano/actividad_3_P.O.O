    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double lado = Double.parseDouble(txtLado.getText());
        double base_mayor = Double.parseDouble(txtBMayor.getText());
        double base_menor = Double.parseDouble(txtBMenor.getText());
        double area = Trapecio.calcular_area(base_mayor, base_menor, lado);
        double perimetro = Trapecio.calcular_perimetro(base_mayor, base_menor, lado);
        txtArea.setText(String.valueOf(area));
        txtPerimetro.setText(String.valueOf(perimetro));
    }//GEN-LAST:event_btnCalcularActionPerformed

