    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double pago_hora = Double.parseDouble(txtPagoHora.getText());
        double horas_trabajadas = Double.parseDouble(txtHoras.getText());
        
        double salario = Empleado.calcular_salario(pago_hora, horas_trabajadas);
        
        if (salario > 450000){
            txtSalario.setText(String.valueOf(salario));
        }
        else {
            txtSalario.setText("No aplica");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

